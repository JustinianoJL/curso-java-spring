package com.curso.java.oo.ejercicio01oo.model;

public class Curso
{
	private
		String identificadorCurso, nombreCurso, descripcionCurso, horario;

	public String getIdentificadorCurso()
	{
		return identificadorCurso;
	}
	public void setIdentificadorCurso(String identificadorCurso)
	{
		this.identificadorCurso = identificadorCurso;
	}
	public String getNombreCurso()
	{
		return nombreCurso;
	}
	public void setNombreCurso(String nombreCurso)
	{
		this.nombreCurso = nombreCurso;
	}
	public String getDescripcionCurso()
	{
		return descripcionCurso;
	}
	public void setDescripcionCurso(String descripcionCurso)
	{
		this.descripcionCurso = descripcionCurso;
	}
	public String getHorario()
	{
		return horario;
	}
	public void setHorario(String horario)
	{
		this.horario = horario;
	}
}